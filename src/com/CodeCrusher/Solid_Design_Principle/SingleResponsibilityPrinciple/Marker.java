package com.CodeCrusher.Solid_Design_Principle.SingleResponsibilityPrinciple;

public class Marker {
	public String name;
	public String color;
	public int year;
	public int price;
	Marker(String name , String color, int year , int price){
		this.name = name;
		this.color = color;
		this.year = year;
		this.price = price;
	}
}
